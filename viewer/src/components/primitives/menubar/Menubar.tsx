const MenubarItem = React.forwardRef<
  React.ElementRef<typeof MenubarPrimitive.Item>,
  React.ComponentPropsWithoutRef<typeof MenubarPrimitive.Item> &
    Pick<React.HTMLAttributes<HTMLButtonElement>, "onClick"> & {
      inset?: boolean;
    }
>(({ className, onSelect, inset, ...props }, ref) => (
  <MenubarPrimitive.Item
    ref={ref}
    slot="button"
    className={cn(
      "relative flex cursor-default select-none items-center rounded-sm px-2 py-1.5 text-sm outline-none focus:bg-primary-4 focus:text-neutral-12 data-[disabled]:pointer-events-none data-[disabled]:opacity-50",
      inset && "pl-8",
      className
    )}
    onSelect={(e) => {
      // eslint-disable-next-line @typescript-eslint/no-unsafe-argument, @typescript-eslint/no-explicit-any
      props.onClick?.(e as any);
      onSelect?.(e);
    }}
    {...props}
  />
));
MenubarItem.displayName = MenubarPrimitive.Item.displayName;

